    private void btnAgendarCitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarCitaActionPerformed

                                               

    // Obtener datos de los campos
    int idCliente, idEmpleado;
    String fechaTexto = inTxtFecha.getText();
    String hora = inTxtHora.getText();

    // Validar campos vac칤os
    if (inTxtIdCliente.getText().isEmpty() || inTxtIdEmpleado.getText().isEmpty() || fechaTexto.isEmpty() || hora.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Validar IDs
    try {
        idCliente = Integer.parseInt(inTxtIdCliente.getText());
        idEmpleado = Integer.parseInt(inTxtIdEmpleado.getText());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID Cliente o ID Empleado inv치lido.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validar fecha
    java.sql.Date fecha;
    try {
        fecha = java.sql.Date.valueOf(fechaTexto);
    } catch (IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, "Formato de fecha inv치lido. Usa YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }


 //conexi칩n David
    private static final String URL = "jdbc:oracle:thin:@localhost:1521:xe";
    private static final String USERNAME = "DraHuellitas";
    private static final String PASSWORD = "1234";


    try (Connection conn = DriverManager.getConnection(url, user, pass)) {
        CallableStatement stmt = conn.prepareCall("{call agendar_cita(?, ?, ?, ?)}");
        stmt.setInt(1, idCliente);
        stmt.setInt(2, idEmpleado);
        stmt.setDate(3, fecha);
        stmt.setString(4, hora);
        stmt.execute();

        JOptionPane.showMessageDialog(this, "Cita agendada correctamente.");

        // 游 Limpiar campos
        inTxtIdCliente.setText("");
        inTxtIdEmpleado.setText("");
        inTxtFecha.setText("");
        inTxtHora.setText("");

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error al agendar cita: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

        
        
